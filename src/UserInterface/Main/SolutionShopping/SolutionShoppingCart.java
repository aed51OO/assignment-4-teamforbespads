    private void solutionTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_solutionTableMousePressed
        // TODO add your handling code here:
        int suppliertablesize = solutionTable.getRowCount();
        int selectedrow = solutionTable.getSelectionModel().getLeadSelectionIndex();

        if (selectedrow < 0 || selectedrow > suppliertablesize - 1) {
            return;
        }
        //this.selected = (SolutionOffer) solutionTable.getValueAt(selectedrow, 0);
        this.clicks ++;
        updateFeedback(this.clicks);

    }//GEN-LAST:event_solutionTableMousePressed

