    private void cartMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cartMousePressed
        int suppliertablesize = cart.getRowCount();
        int selectedrow = cart.getSelectionModel().getLeadSelectionIndex();

        if (selectedrow < 0 || selectedrow > suppliertablesize - 1) {
            return;
        }
        this.selectedInCart = (SolutionOffer) cart.getValueAt(selectedrow, 0);
    }//GEN-LAST:event_cartMousePressed

