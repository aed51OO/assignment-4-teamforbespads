    private void refreshSolutionTable() {
        // Clear existing data
        DefaultTableModel model = (DefaultTableModel) solutionTable.getModel();
        model.setRowCount(0);

        // Iterate through the SolutionOfferCatalog and add rows to the table
        for (SolutionOffer so : this.soc.getSolutionoffers()) { // Assuming getSolutionOffers() returns a list of SolutionOffer
            model.addRow(new Object[]{so.getSolutionName(), so.getFloor(), so.getCeiling(), so.getTarget()});
        }
    }

    private void clearShoppingCart() {
        DefaultTableModel model = (DefaultTableModel) cart.getModel();
        model.setRowCount(0);
    }

